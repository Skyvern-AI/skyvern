import { describe, expect, it } from "vitest";
import {
  CredentialParameterState,
  computeWrappedCredentialIds,
  isAutoGeneratedCredentialKey,
} from "./types";

describe("isAutoGeneratedCredentialKey", () => {
  it("matches the auto-generated key scheme", () => {
    expect(isAutoGeneratedCredentialKey("credentials")).toBe(true);
    expect(isAutoGeneratedCredentialKey("credentials_1")).toBe(true);
    expect(isAutoGeneratedCredentialKey("credentials_42")).toBe(true);
  });

  it("does not match user-authored keys", () => {
    expect(isAutoGeneratedCredentialKey("my_login")).toBe(false);
    expect(isAutoGeneratedCredentialKey("prod_credentials")).toBe(false);
    expect(isAutoGeneratedCredentialKey("credentials_")).toBe(false);
    expect(isAutoGeneratedCredentialKey("credentialsX")).toBe(false);
  });
});

describe("computeWrappedCredentialIds", () => {
  const skyvern = (
    key: string,
    credentialId: string,
  ): CredentialParameterState => ({
    key,
    parameterType: "credential",
    credentialId,
  });

  it("wraps only user-authored Skyvern credential params", () => {
    const params = [
      skyvern("credentials", "cred-auto"),
      skyvern("credentials_1", "cred-auto-2"),
      skyvern("my_login", "cred-user"),
    ];
    const wrapped = computeWrappedCredentialIds(params);
    expect(wrapped.has("cred-user")).toBe(true);
    expect(wrapped.has("cred-auto")).toBe(false);
    expect(wrapped.has("cred-auto-2")).toBe(false);
  });

  it("ignores non-Skyvern credential params", () => {
    const params: Array<CredentialParameterState> = [
      {
        key: "bw_login",
        parameterType: "credential",
        collectionId: "c",
        itemId: "i",
        urlParameterKey: null,
      },
      {
        key: "op_login",
        parameterType: "onepassword",
        vaultId: "v",
        itemId: "i",
      },
    ];
    expect(computeWrappedCredentialIds(params).size).toBe(0);
  });

  it("wraps a user param even when its key equals the credential name", () => {
    const wrapped = computeWrappedCredentialIds([skyvern("Github", "cred-gh")]);
    expect(wrapped.has("cred-gh")).toBe(true);
  });
});
